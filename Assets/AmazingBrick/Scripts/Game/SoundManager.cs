	public void PlayMusicGame()
		{
			music.volume = 1;


			PlayMusic (musicGame);
		}
		/// <summary>
		/// Play the music game over
		/// </summary>
		public void PlayMusicGameOver()
		{
			playFX (musicGameOver);
			music.volume = 0;
			//		music.DOFade(1,0);
		}
		/// <summary>
		/// Play the music menu
		/// </summary>
		public void PlayMusicMenu()
		{
			PlayMusic (musicMenu);
		}
		/// <summary>
		/// Play the jump fx
		/// </summary>
		public void PlayJumpFX()
		{
			playFX (jumpFX, 0.4f);
		}

		void OnSetDiamond(int i)
		{
			PlayCoinFX();
		}
