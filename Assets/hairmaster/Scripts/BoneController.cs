        public int downScaleHair()
        {
            int childS = 0;
            Transform parent = this.transform.GetChild(0);
        
            while (parent != null)
            {
                parent = parent.GetChild(0).transform;
                if (parent.GetComponent<BoneController>()!=null)
                    
                    
                    childS++;
                else
                {
                    hairBoneController.cutHair(childS);
                    return childS;
                }

                
                
            }
            return childS;
        }

