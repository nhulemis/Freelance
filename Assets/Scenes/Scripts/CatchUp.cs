    IEnumerator Movement()
    {
        while (true)
        {
            int rdx = Random.Range(-1, 2);
            int rdy = Random.Range(-1, 2);
            int mainX = Random.Range(0, Screen.width / 2);
            int mainY = Random.Range(0, Screen.height / 2);
            GetComponent<RectTransform>().DOAnchorPos(new Vector2(rdx * mainX, rdy * mainY), 1).SetEase(Ease.Linear);
            transform.DOLocalRotate(new Vector3(0, 0, 360 * rdx), 0.5f, RotateMode.FastBeyond360).SetRelative(true)
                .SetEase(Ease.Linear);
            
            yield return new WaitForSeconds(.5f);
        }
    }

    public void OnPointerDown(PointerEventData eventData)
